#Use a for loop and random.randrange to print 10 random integers between 25 and 35
import random

#Use a for loop to iterate 10 times
for _ in range(10):
    random_int = random.randrange(25, 36)  # Generates random integer between 25 and 35 (inclusive)
    print(random_int)

